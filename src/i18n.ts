export function t(key: string, vars?: Record<string, string | number>): string {
  const entry = dict[key]
  if (!entry) return key
  let text = entry[locale] ?? entry.en ?? key
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.replace(`{${k}}`, String(v))
    }
  }
  return text
}
